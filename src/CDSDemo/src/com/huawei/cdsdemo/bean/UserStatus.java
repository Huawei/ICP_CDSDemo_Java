package com.huawei.cdsdemo.bean;

public class UserStatus
{
    private String talkGroupNumber;
    
    private String userNumber;
    
    private String operationType;
    
    private String operationState;
    
    private String operatorNumber;
    
    private String operatorName;
    
    private String isGroupCallHandler;
    
    public String getTalkGroupNumber()
    {
        return talkGroupNumber;
    }
    
    public void setTalkGroupNumber(String talkGroupNumber)
    {
        this.talkGroupNumber = talkGroupNumber;
    }
    
    public String getUserNumber()
    {
        return userNumber;
    }
    
    public void setUserNumber(String userNumber)
    {
        this.userNumber = userNumber;
    }
    
    public String getOperationState()
    {
        return operationState;
    }
    
    public void setOperationState(String operationState)
    {
        this.operationState = operationState;
    }
    
    public String getOperationType()
    {
        return operationType;
    }
    
    public void setOperationType(String operationType)
    {
        this.operationType = operationType;
    }
    
    public String getOperatorNumber()
    {
        return operatorNumber;
    }
    
    public void setOperatorNumber(String operatorNumber)
    {
        this.operatorNumber = operatorNumber;
    }
    
    public String getOperatorName()
    {
        return operatorName;
    }
    
    public void setOperatorName(String operatorName)
    {
        this.operatorName = operatorName;
    }
    
    public String getIsGroupCallHandler()
    {
        return isGroupCallHandler;
    }
    
    public void setIsGroupCallHandler(String isGroupCallHandler)
    {
        this.isGroupCallHandler = isGroupCallHandler;
    }
    
}
